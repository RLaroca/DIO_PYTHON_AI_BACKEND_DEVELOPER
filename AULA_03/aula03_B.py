#Operadores de Atribuição em Python

#Atribuição simples =
nome = "rafael"
print(nome)

#Atribuição com adicção
valor = 500
valor+=50
print(valor)
#Atribuição com subtraçao
valor_2 = 400
valor_2-=100
print(valor_2)
#Atribuição com divisão
valor_3 = 10
valor_3 /=3
print(valor_3)
#Atribuição com multiplicação
valor_4 = 2
valor_4 *=2
print(valor_4)
#Atribuição com expenenciação
valor_5 = 2
valor_5 **=3
print(valor_5)
