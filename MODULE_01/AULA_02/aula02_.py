#TIPOS DE DADOS
##built In mais comuns:
## texto | str
## numérico | int, float, complex
## sequência | list, tuple, range
## mapa (chave:valor) | dict
## coleção | set, fronzenset
## booleano (Subclasse de Int, onde: TRUE = 1 e FALSE = 0) | bool
    ## Em python, qualquer numero maior que zero representa TRUE
## binário | byte, bytearray, memoryview

print(1+1==0) #retorna um boolenao
print(1+1) #retorna um int
print(1+1.5) #retorna um float
print("rafael") #retorna uma string

# O tipo do dado informa ao interpretador a quantidade de memória que será utilizada para o processamento desse dados
# Cada tipo de dado é uma classe e carrega um conjnto de métodos que podem ser aplicados