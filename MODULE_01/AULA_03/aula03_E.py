#Operadores de associação
## Utilizado em sequências de caracteres ou objetos
lista = ["banana", "maçã", "laranja"]
frase = "Olá Mundo!"
valores = [1, 100, 75]
##Operador IN e NOT IN
print("banana" in lista, "á" in frase, 0 in valores)
print("abacate"not in lista, "mundo" not in frase, "Mundo" not in frase, 1 not in valores)